// このコードは三角形の面積を求めるプログラムです。
var w = 3; // 三角形の底辺
var h = 10; // 三角形の高さ
var area = (w * h) / 2; // 三角形の面積の公式「（底辺）×（高さ）÷２」を使って計算している
document.write(area); // 面積を出力する
document.write("<br>");

